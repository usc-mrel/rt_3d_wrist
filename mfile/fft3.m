function [in] = fft3(in)
%--------------------------------------------------------------------------
%   [in] = fft3(in)
%--------------------------------------------------------------------------
%   Compute 3D fft
%--------------------------------------------------------------------------
%   Inputs:
%       - in    [sx, sy, sz, ...]
%                       
%       - in    array with at least three dimensions
%--------------------------------------------------------------------------
%   Output:
%       - in    [sx, sy, nof, ~]
%
%       - in    3D fft of input
%--------------------------------------------------------------------------
%   Author:
%       Ye Tian
%       E-mail: phye1988@gmail.com
%--------------------------------------------------------------------------

in = fft(in,[],1);
in = fft(in,[],2);
in = fft(in,[],3);
end